    /**
     *
     */
    public static class HitsUI implements StatisticsUI {

        private HitsPanel panel;
        private Hits hits;

        public HitsUI() {
            panel = new HitsPanel();
        }

        /**
         *
         */
        public void unsetup() {
            //Set params
            try {
                double epsilon = Double.parseDouble(panel.epsilonTextField.getText());
                double probability = Double.parseDouble(panel.probTextField.getText());
                hits.setEpsilon(epsilon);
                hits.setProbability(probability);

            } catch (Exception e) {
            }
        }

        /**
         *
         * @return
         */
        public JPanel getPanel() {
            return panel;
        }

        /**
         *
         * @param statistics
         */
        public void setup(Statistics statistics) {
            this.hits = (Hits) statistics;

            panel.epsilonTextField.setText("" + hits.getEpsilon());
            panel.probTextField.setText("" + hits.getProbability());
        }
    }
