package org.gephi.preview.api;

/**
 * Point interface.
 *
 * @author Jérémy Subtil <jeremy.subtil@gephi.org>
 */
public interface Point {

    /**
     * Returns the x coordinate.
     *
     * @return the x coordinate
     */
    public Float getX();

    /**
     * Returns the y coordinate.
     *
     * @return the y coordinate
     */
    public Float getY();
}
